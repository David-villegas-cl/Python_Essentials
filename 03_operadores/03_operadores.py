# EJERCICIOS DE OPERADORES

# 1. Declara dos variables numéricas (con el valor que desees), muestra por consola la suma, resta, multiplicación, división y módulo (resto de la división) de ambas.

# 2. Declara dos variables numéricas (con el valor que desees), muestra por consola cada una de las siguientes comparaciones: mayor que, menor que, mayor o igual que, menor o igual que y diferente de.

# 3. Declara dos variables booleanas ( con el valor que desees), realiza la operación de AND, OR y NOT. Muestra por consola el resultado de cada operación.

# 4. Declara una variable tipo lista con 5 elementos, y utiliza los operadores de pertenencia (in, not in) para comprobar si un elemento se encuentra en la lista.

# 5. Declara una variable tipo numérica con el valor que desees, y utiliza cada uno de los operadores de asignación (+=, -=, *=, /=, %=, **=) para modificar el valor de la variable.
